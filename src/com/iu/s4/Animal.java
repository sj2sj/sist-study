package com.iu.s4;

public class Animal {
	
	private String name;
	private int age;
	
	public void sound() {};

	
	
	/* setter & getter */
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
}
